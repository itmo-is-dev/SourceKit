                {
                    new SourceFile(GeneratedArrayBuilderName, GeneratedArrayBuilder),
                },
                AdditionalReferences = { typeof(GenerateBuilderAttribute).Assembly },
                ReferenceAssemblies = ReferenceAssemblies.Net.Net60,
            },
        };

        await test.RunAsync();
    }

